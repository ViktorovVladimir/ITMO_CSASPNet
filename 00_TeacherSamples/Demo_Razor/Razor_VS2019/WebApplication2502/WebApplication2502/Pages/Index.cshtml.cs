using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using Microsoft.EntityFrameworkCore;
using WebApplication2502.Model;

namespace WebApplication2502.Pages
{
    public class IndexModel : PageModel
    {
        private readonly WebApplication2502.Model.CustomerDbContext _context;

        public IndexModel(WebApplication2502.Model.CustomerDbContext context)
        {
            _context = context;
        }

        public IList<Customer> Customer { get;set; }

        public async Task OnGetAsync()
        {
            Customer = await _context.Customers.ToListAsync();
        }
    }
}
